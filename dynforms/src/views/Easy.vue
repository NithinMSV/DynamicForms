<template>
  <div class="easymode">
    <h1>Easy Mode</h1>
    <!-- <p>{{steps[incr]}}</p>
        <p>{{incr}}</p> -->
    <div v-if="incr === 0">
      <h1>I'm Type</h1>
      <EasyType />
    </div>

    <div v-if="incr === 1">
      <h1>Find your {{ message.toLowerCase() }} templates here!</h1>
      <EasyTemplate />
    </div>
    <div v-if="incr === 2">
      <h1>I'm Fields</h1>
    </div>
    <b-button
      variant="success"
      @click="prev"
      :disabled="incr <= 0 && selectedRadio"
      >Back</b-button
    >
    <b-button
      class="ml-2"
      variant="success"
      @click="next"
      :disabled="incr >= steps.length - 1 || !selectedRadio"
      >Next</b-button
    >
  </div>
</template>

<script>
import EasyType from "../components/CreateOptions/EasyMode/easytype";
import EasyTemplate from "../components/CreateOptions/EasyMode/easytemplate";

export default {
  name: "EasyMode",
  components: {
    EasyType,
    EasyTemplate,
  },
  data() {
    return {
      search: "",
      types: [],
      radioselected: "",
      incr: 0,
      steps: ["Type", "Template", "Fields"],
      selected: "first",
    };
  },
  methods: {
    prev() {
      return this.incr--;
    },
    next() {
      return this.incr++;
    },
    selectedRadio: function() {
      this.$store.getters.TypeSelected !== null;
    },
  },
  computed: {
    message() {
      return this.$store.getters.TypeSelected;
    },
  },
};
</script>

